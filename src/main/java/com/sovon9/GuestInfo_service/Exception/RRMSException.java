package com.sovon9.GuestInfo_service.Exception;

public class RRMSException extends RuntimeException{

	public RRMSException() {
		super();
	}

	public RRMSException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public RRMSException(String message, Throwable cause) {
		super(message, cause);
	}

	public RRMSException(String message) {
		super(message);
	}

	public RRMSException(Throwable cause) {
		super(cause);
	}

	
	
}
